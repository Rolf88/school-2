public class User {
    String name;
    String id;
    String pwd;
    String gender;
    String age;
    String country;

    public User(String name, String id, String pwd, String gender, String age, String country) {
        this.name = name;
        this.id = id;
        this.pwd = pwd;
        this.gender = gender;
        this.age = age;
        this.country = country;
    }

    public User() {
    }
    
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
    
    
}
